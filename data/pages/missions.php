<h1>&nbsp;</h1>

<h1>Community Service<img alt="" src="data/img/imghere.png" style="float:right; height:209px; margin-left:1em; width:215px"></h1>

<p>We desire to impact the community around us through our local outreach projects. We partner with local organizations and churches to reach and serve those who are in need around us. Past projects we have been involved in include: <a href="http://www.booksforafrica.org/">Books for Africa</a>, visiting <a href="http://rossmemorial.com/">Ross Memorial Nursing Home</a>, hosting a block party at Sedgefield Apartment homes, assisting at Camino de Vida Kid Zone and English classes, and being involved in <a href="http://www.handsonatlanta.org/Mission_and_Values">Hands on Atlanta </a>at Hollydale Elementary School.&nbsp;</p>

<p>Serving others in the community is a great way to get to know those in our community as well as each other. We always have plenty of community service opportunities. Stay tuned for more service projects coming up on our events page, and feel free to come serve with us when you can!</p>
