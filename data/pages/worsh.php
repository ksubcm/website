<div class="row">
<h1>What We Do.</h1>

<p>We would love for you to come join us for our weekly Fusion Worship Services at <a class="hyperlink" href="http://www.kfbc.org/" target="_blank">Kennesaw First Baptist Church</a> where we get to praise God through singing and by opening his word. Other worship events have included Harvest Night last semester at SPSU, which was a night of praise on Halloween night that incorporated the spirit of the Harvest by giving thanks and joining with others. Other worship events will be posted on our events page. If you are interested in helping lead worship or helping to coordinate Fusion (sound, lights, tech) please contact us and we will assist you as soon as we can.</p>
