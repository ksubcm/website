<h1>What is Discipleship?</h1>

<hr>
<p>Discipleship begins as Christ gives us a double call to follow him. Immediately, James and John left their boat and what they were doing and their family and followed Jesus. Here, at the united campuses of Kennesaw State and Southern Polytechnic State University, the Baptist Collegiate Ministry has a heart for the Lord and to see our friends, family, coworkers and colleagues know Jesus and his Word. In pursuit of Christ through discipleship, the BCM has found itself at bowling alleys, fireside chats by the Chattahoochee River, in apartments, with international students down a river and down the slopes, at lunches, and in countless conversations witnessing the Gospel of Jesus Christ to whomever we meet. Our heart is to know God. Our knowledge of God depends on the sustained initiative of God in knowing me. He knows me as a friend, one who loves me; and there is not a moment when his eye is off me or when his care falters. I am graven on the palms of his hands, Isaiah 49. And Jesus says, "<em>Follow me</em>." His call harmonizes with our inherent depth. <em>Look, here's the treasure. It's me. </em>Then we are awakened, muddy hands and all, wallowing in the slums this whole time but now testifying of a "desire for something infinitely great and beautiful and valuable and satisfying — the name and the glory of God. So we leave and we follow. Goodbye broken cisterns (Jeremiah 2:13), hello my exceeding joy (Psalm 43:4).</p>

